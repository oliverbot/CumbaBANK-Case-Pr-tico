  Raises `Ecto.NoResultsError` if the User does not exist.

  ## Examples

      iex> get_user!(123)
      %User{}

      iex> get_user!(456)
      ** (Ecto.NoResultsError)

  """
  def get_user!(id), do: Repo.get!(User, id)

  @doc """
  Creates a user.

  ## Examples

      iex> create_user(%{field: value})
      {:ok, %User{}}

      iex> create_user(%{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def create_user(attrs \\ %{}, account) do
    attrs =
      attrs
      |> Map.put("account_id", account.id)

    %User{}
    |> User.changeset(attrs)
    |> Repo.insert()
  end

  def create_user_account(account_params, user_params) do
    Repo.transaction(fn ->
      with {:ok, %Account{} = account} <- create_account(account_params),
           {:ok, %User{} = user} <- create_user(user_params, account) do
            data =
              Map.merge(user, account)

            data
      else
        {:error, changeset} -> Repo.rollback(changeset)
        _ -> Repo.rollback(:missing_statements)
      end
    end)
  end

  def get_login([{field, value}]) do
    query =
      from(
        u in User,
        where: field(u, ^field) == ^value
      )

    Repo.one(query)
  end

  def login({:email, email}, password) do
    user = get_login(email: email)

    if user && user.password_hash do
      Pbkdf2.check_pass(user, password)
    else
      {:error, "user has no password"}
    end
  end

